    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        if (soma == true){
            num2 = Float.parseFloat(jTextField1.getText());
            resultado = num1 + num2;
            String mensagem = String.format("%s",resultado);
            jTextField1.setText(mensagem);
            soma = false;
            
        }
       if (subt == true ){
           num2 = Float.parseFloat(jTextField1.getText());
           resultado = num1 - num2;
           String mensagem = String.format("%s",resultado);
           jTextField1.setText(mensagem);
           subt = false;
       }
       if (mult == true ){
           num2 = Float.parseFloat(jTextField1.getText());
           resultado = num1 * num2;
           String mensagem = String.format("%s",resultado);
           jTextField1.setText(mensagem);
           mult = false;
       }
       if (divi == true ){
           num2 = Float.parseFloat(jTextField1.getText());
           resultado = num1 / num2;
           String mensagem = String.format("%s",resultado);
           jTextField1.setText(mensagem);
           divi = false;
       }
    }//GEN-LAST:event_btnIgualActionPerformed

